# Opgave 3: functie die berekeningen uitvoert en teruggeeft als lijst
def mijn_functie_2(getal_1, getal_2):
    return [getal_1 + getal_2, getal_1 - getal_2, getal_1 * getal_2, round(getal_1 / getal_2)]